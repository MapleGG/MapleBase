package scd.entity;

public class Employee {

	private String employee_code;
	private String employee_name;
	private String employee_title;
	private String employee_line;
	private String employee_sex;
	private String employee_province;
	private String employee_volk;
	private String employee_education;
	
	private String employee_scode;
	public String getEmployee_code() {
		return employee_code;
	}
	public void setEmployee_code(String employee_code) {
		this.employee_code = employee_code;
	}
	public String getEmployee_name() {
		return employee_name;
	}
	public void setEmployee_name(String employee_name) {
		this.employee_name = employee_name;
	}
	public String getEmployee_title() {
		return employee_title;
	}
	public void setEmployee_title(String employee_title) {
		this.employee_title = employee_title;
	}
	public String getEmployee_line() {
		return employee_line;
	}
	public void setEmployee_line(String employee_line) {
		this.employee_line = employee_line;
	}
	public String getEmployee_sex() {
		return employee_sex;
	}
	public void setEmployee_sex(String employee_sex) {
		this.employee_sex = employee_sex;
	}
	public String getEmployee_province() {
		return employee_province;
	}
	public void setEmployee_province(String employee_province) {
		this.employee_province = employee_province;
	}
	public String getEmployee_volk() {
		return employee_volk;
	}
	public void setEmployee_volk(String employee_volk) {
		this.employee_volk = employee_volk;
	}
	public String getEmployee_education() {
		return employee_education;
	}
	public void setEmployee_education(String employee_education) {
		this.employee_education = employee_education;
	}
	public String getEmployee_scode() {
		return employee_scode;
	}
	public void setEmployee_scode(String employee_scode) {
		this.employee_scode = employee_scode;
	}
	
	
	
}
